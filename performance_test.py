def pos(graph, pos):
    return graph[pos[0]][pos[1]]


def run_trial(graph, algorithm, Verbose=True):
    assert algorithm is 'BFS' or algorithm is 'ASTAR', "Invalid algorithm, must be algorithm='BFS' or algorithm='ASTAR'"
    start = (random.randint(0, len(graph) - 1), random.randint(0, len(graph[0]) - 1))
    goal = (random.randint(0, len(graph) - 1), random.randint(0, len(graph[0]) - 1))
    log('Finding valid start position.', Verbose)
    while pos(graph, start) == 1:
        start = (random.randint(0, len(graph) - 1), random.randint(0, len(graph[0]) - 1))
    log('Valid start position found.', Verbose)
    log('Finding valid target position.', Verbose)
    while pos(graph, goal) == 1:
        goal = (random.randint(0, len(graph) - 1), random.randint(0, len(graph[0]) - 1))
    log('Valid target position found.', Verbose)
    log('Start: ' + str(start), Verbose)
    log('Target: ' + str(goal), Verbose)
    if algorithm is 'BFS':
        log('Running BFS pathfinding...', Verbose)
    elif algorithm is 'ASTAR':
        log('Running A* pathfinding...', Verbose)

    t_0 = time()
    if algorithm is 'BFS':
        path = pathing.bfs_pathing(graph=graph, start=start, goal=goal)
